# Definitions for IB Pico projects

# GPIOs

# Controller I/O
PIN_DAC0 =  const(25) # Is Physical pin 9
PIN_DAC1 =  const(26) # Is Physical pin 10

PIN_ADC1_5 =  const(33) # Is Physical pin 8
PIN_ADC2_7 =  const(27) # Is Physical pin 11
PIN_ADC2_3 =  const(15) # Is Physical pin 4

PIN_BLNK0 = const(14)
PIN_BLNK1 = const(12)

PIN_DIR = const(22)

# Mappings
PIN_BEMF0 = PIN_ADC1_5
PIN_BEMF1 = PIN_ADC2_7
PIN_POT = PIN_ADC2_3



